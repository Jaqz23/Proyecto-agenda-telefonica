    public void agregarBD() {
    if (!tfnombre.getText().isEmpty() && !tfapellido.getText().isEmpty() && !tfTelefono.getText().isEmpty()) {
        String nom = tfnombre.getText();
        String ape = tfapellido.getText();
        String cor = tfcorreo.getText();
        String emp = tfempresa.getText();
        String num = tfTelefono.getText();

        // Verificar si el número de teléfono tiene exactamente 10 dígitos
        if (num.length() != 10) {
            JOptionPane.showMessageDialog(null, "El número de teléfono debe tener exactamente 10 dígitos");
            return; // Salir del método si el número no tiene 10 dígitos
        }

        try {
            // Verificar si el número de teléfono ya existe en la base de datos
            String sqlCheckExistence = "SELECT COUNT(*) FROM agenda WHERE numero = ?";
            conet = con1.getConnection();
            PreparedStatement pstCheckExistence = conet.prepareStatement(sqlCheckExistence);
            pstCheckExistence.setString(1, num);
            ResultSet rsCheckExistence = pstCheckExistence.executeQuery();
            rsCheckExistence.next();
            int count = rsCheckExistence.getInt(1);
            if (count > 0) {
                JOptionPane.showMessageDialog(null, "El número de teléfono ya está en la agenda");
                return; // Salir del método si el número ya existe
            }

            // Insertar el nuevo contacto en la base de datos
            String sql = "INSERT INTO agenda(nombre, apellido, correo, empresa, numero) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conet.prepareStatement(sql);
            pst.setString(1, nom);
            pst.setString(2, ape);
            pst.setString(3, cor);
            pst.setString(4, emp);
            pst.setString(5, num);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Nuevos datos agregados");

            limpiarCamposTexto();

        } catch (HeadlessException | SQLException e) {
            // Manejar las excepciones
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al agregar datos: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Ingrese los datos que se piden");
    }
}



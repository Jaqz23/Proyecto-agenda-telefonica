    public void buscar() {
        String texto = tfBuscar.getText().trim();

        RowFilter<DefaultTableModel, Object> filtro = RowFilter.regexFilter("(?i)" + texto);

        trs.setRowFilter(filtro);

    }

    

    public void consultar() {
        String sql = "select * from agenda";

        try {
            conet = con1.getConnection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] agenda = new Object[6];
            modelo = (DefaultTableModel) Tabla.getModel();
            while (rs.next()) {
                agenda[0] = rs.getInt("id");
                agenda[1] = rs.getString("nombre");
                agenda[2] = rs.getString("apellido");
                agenda[3] = rs.getString("correo");
                agenda[4] = rs.getString("empresa");
                agenda[5] = rs.getString("numero");

                modelo.addRow(agenda);

            }
            Tabla.setModel(modelo);

        } catch (SQLException e) {
        }

    }


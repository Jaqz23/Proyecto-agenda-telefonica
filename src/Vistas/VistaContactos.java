    public void limpiarRegistro() {
        int filaSeleccionada = Tabla.getSelectedRow();

        if (filaSeleccionada < 0) {
            JOptionPane.showMessageDialog(null, "Selecciona el registro que quieres eliminar");
        } else {
            int idRegistro = (int) Tabla.getValueAt(filaSeleccionada, 0);
            String sqlDelete = "DELETE FROM agenda WHERE id = " + idRegistro;

            try {
                conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sqlDelete);  // Eliminar el registro seleccionado

                // Reiniciar el contador de ID en la tabla agenda
                String sqlResetAutoIncrement = "ALTER TABLE agenda AUTO_INCREMENT = 1";
                st.executeUpdate(sqlResetAutoIncrement);

                // Actualizar la tabla después de borrar los registros
                modelo = (DefaultTableModel) Tabla.getModel();
                modelo.setRowCount(0);  // Eliminar todas las filas de la tabla
                Tabla.setModel(modelo);

                consultar();

            } catch (SQLException e) {
            }
        }

    }

